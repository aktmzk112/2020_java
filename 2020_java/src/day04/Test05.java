package day04;

import java.util.*;
public class Test05 {
	/*
	 *  알파벳을 입력받아서
	 *  대문자이면 "입력받은 문자는 O는 대문자 입니다"
	 *  소문자이면 "입력받은 문자는 O는 소문자 입니다"
	 *  출력
	 *  
	 *  Scanner 에서 문자를 추출해주는 함수는 존재하지 않습니다
	 *  따라서 문자열로 추출헌후에 첫번째 자리의 문자만 추출허여 사용해야 한다 
	 *  charAt(위치값) ==> 문자열의 위치값에 들어있는  문자를 추출해주는 함수
	 *  	참고 ] 
	 *  		자바에서 사용하는 위치값은 index라고 부르며 0부터 시작한다.
	 *  
	 *  indexOf(문자 또는 문자열)  
	 */
	public static void main(String[] args) {
		String str = "class";
		
		//첫뻔째 문자 추출하기

		char ch = str.charAt(0);
		System.out.println("str의 첫뻔째 문자 "+ ch);
		
		// a의 위치값 알아내기
		int idx = str.indexOf('a');
		System.out.println("a의 위치는 : "+ idx + "입니다.");
				
		// 입력받을 준비 
		Scanner sc = new Scanner(System.in);
		
		//메세지 출력
		System.out.println("알파벳을 입력하세요 : ");
		String str9 = sc.nextLine();
		
		// 문자로 추출
		char ch1 = str9.charAt(0);
		
		String str2 = (ch1 >= 'a') ? "소" : "대"; 
		
		String str3 = "입력된 문자 " + ch1 + "은 " + str2 + "문자 입니다.";
		
		System.out.println(str3);
		
		
		//3항 조건연산자의 중첩사용
		String str4 = (ch1 >= 'a') ? ((ch1 <= 'z') ? "소":"특수") : "대"; 
		System.out.println("입력된 문자 " + ch1 + "은 " + str4 + "문자 입니다.");
		
		// 자바에서 비교는 한번에 한개만 가능하다
		// a > b > c => (x)
		// a > b
		// b > c
		
		
	
	}
	
}
